package cursoJava.executavel;

public class EstruturaDeRepeticao {
	public static void main(String[] args) {
		// while, do while

		int num = 0;
		while (num <= 20) {
			//System.out.println(num);
			num++;
		}

		do {
		//System.out.println(num);
		num++;
		}while(num <= 20);
		
		// for
		// inicializar / repetir ate / acrescimo 
		for(int i = 10;i >= 1; i--) {
			System.out.println(i);
		}
		

	}
}
